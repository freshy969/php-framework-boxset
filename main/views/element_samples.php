                    },
                    cancel: function () {
                        console.log('Negative clicked');
                    }
                });
            });
            $('button[data-action=show_browser]').click(function () {
                var html = '';
                for (var name in $.browser) {
                    html += name + ': ' + $.browser[name] + '<br/>';
                }
                $.alert(html);
            });
        });
